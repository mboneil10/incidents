  ChairOnLift enum('yes', 'no') NOT NULL DEFAULT 'no',
  LiftDeployed enum('yes', 'no') NOT NULL DEFAULT 'no',
  PassengersPresent varchar(3) DEFAULT NULL,
  SeatBelts varchar(3) DEFAULT NULL,
  PointOfContact varchar(100) DEFAULT NULL,
  CurbDist varchar(100) DEFAULT NULL,
  VehicleDistance varchar(100) DEFAULT NULL,
